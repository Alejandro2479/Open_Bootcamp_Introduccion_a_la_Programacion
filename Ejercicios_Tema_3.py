# Primera Parte

def sumar_tres_numeros(a, b, c):
    resultado = a + b + c
    return resultado

# Llamando a la función con valores
resultado = sumar_tres_numeros(1, 2, 3)
print(resultado) # Output: 6

# Segunda Parte
class Coche:
    def __init__(self, num_puertas):
        self.num_puertas = num_puertas
    
    def agregar_puerta(self):
        self.num_puertas += 1

# Creando un objeto miCoche y agregando una puerta
miCoche = Coche(3)
miCoche.agregar_puerta()

# Mostrando el número de puertas del objeto miCoche
print(miCoche.num_puertas) # Output: 4
